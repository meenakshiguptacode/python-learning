# range()
# function returns a sequence of numbers , starting from 0 by default , and increamemts it by 1 and stops before a specified number 

# range(start? , stop, step ?)

for el in range(5):
    print(el)
 
for el in range(1,5):
    print(el)
    
for el in range(1,5,2):
    print(el)
    
    
for el in range(1,10):
    print(el)

